package ru.svdegron.java2021.sort;

public class InsertionSort implements Sortable {

	@Override
	public int[] AscendingFromBegin(int[] input) {

		int[] output = new int[input.length];

		return output;

	}

	@Override
	public int[] DescendingFromEnd(int[] input) {

		int[] output = new int[input.length];

		return output;

	}

}
